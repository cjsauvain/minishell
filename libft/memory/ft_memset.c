#include "../libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = s;
	while (i < n)
	{
		*(char *)ptr = c;
		ptr++;
		i++;
	}
	return (s);
}
